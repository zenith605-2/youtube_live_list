const SUPPORTED_LANGS = ['en', 'ja', 'ko', 'zh', 'es'];
const DEFAULT_LANG = 'en';

function detectInitialLang() {
  const saved = localStorage.getItem('lang');
  if (saved && SUPPORTED_LANGS.includes(saved)) return saved;
  return DEFAULT_LANG;
}

let currentLang = detectInitialLang();

function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);
}

function t(key, vars) {
  let str = translations[currentLang]?.[key] ?? translations[DEFAULT_LANG][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, v);
    }
  }
  return str;
}

function applyStaticTranslations() {
  document.documentElement.lang = currentLang;
  // 정적 SEO 페이지(/c/*, /country/*)는 자체 제목을 유지한다
  if (!window.__presetCountry && !window.__presetCategory) document.title = t('site_title');
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
}
